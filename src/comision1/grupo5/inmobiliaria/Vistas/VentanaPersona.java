    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
     try {
         Connection con = conexion.getCon();
         String sql = "UPDATE persona SET nombre = ?, dni = ?, celular = ? WHERE id_persona = ?;";
         PreparedStatement ps = con.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
         ps.setString(1, t_nom.getText());
         ps.setString(2, t_dni.getText());
         ps.setString(3, t_celu.getText());
         ps.setString(4, t_id.getText());
         ps.executeUpdate();
         ps.close();
     } catch (SQLException ex) {
         Logger.getLogger(VentanaPersona.class.getName()).log(Level.SEVERE, null, ex);
     }
            limpiar();   
        
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String nom = t_nom.getText();
         Persona persona2 = personaData.buscarPersona(nom);
        if(persona2 != null){
            t_id.setText(persona2.getId_persona()+"");
            t_nom.setText(persona2.getNombre());
            t_dni.setText(persona2.getdni()+"");
            t_celu.setText(persona2.getCelular()+"");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

